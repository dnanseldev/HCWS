 SELECT COD_CENCUSTO,
       CC.NOM_CENCUSTO,
       COUNT(*) QTD
 FROM CENTRO_CUSTO_BEM_PATRIMONIAL CCBP
 INNER JOIN CENTRO_CUSTO CC USING (COD_CENCUSTO)
 WHERE CCBP.DTA_FIM IS NULL
 GROUP BY COD_CENCUSTO,
          CC.NOM_CENCUSTO
 ORDER BY QTD DESC
